    private void correr_barritaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_correr_barritaMouseClicked
        // TODO add your handling code here:

        //hilo del cronometro
        hiloTiempo h = new hiloTiempo(texto_hora);
        Thread proceso1 = new Thread(h);
        proceso1.start();

        //obtener la velocidad de los carros seleccionados 
        int velo1 = ((carro) combo_1.getSelectedItem()).getVelocidad();
        int velo2 = ((carro) combo_2.getSelectedItem()).getVelocidad();

        //obetener los nombres 
        String j1 = ((carro) combo_1.getSelectedItem()).getMarca();
        String j2 = ((carro) combo_2.getSelectedItem()).getMarca();

        juga1.setText(j1);
        juga2.setText(j2);

        //arrancar el HiloBarrita para cada carro seleccionado
        hiloBarrita hiloBarra1 = new hiloBarrita(barrita1, velo1);
        Thread proceso2 = new Thread(hiloBarra1);
        proceso2.start();

        hiloBarrita hiloBarra2 = new hiloBarrita(barrita2, velo2);
        Thread proceso3 = new Thread(hiloBarra2);
        proceso3.start();

        // determinar el ganador
        String ganador;
        if (barrita1.getValue() >= 100) {
            ganador = j1;
        } else {
            ganador = j2;
        }

        JOptionPane.showMessageDialog(dialogo_competencia, "El ganador es: " + ganador);
    }//GEN-LAST:event_correr_barritaMouseClicked

