    private void combo_1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combo_1ItemStateChanged
        // TODO add your handling code here:
        DefaultComboBoxModel mi = (DefaultComboBoxModel) combo_1.getModel();
        DefaultComboBoxModel ma = (DefaultComboBoxModel) combo_2.getModel();

        if (evt.getStateChange() == ItemEvent.SELECTED) {
            carro carSelected = (carro) combo_1.getSelectedItem();
            if (carSelected != null) {
                texto_ma.setText(carSelected.getMarca());
                texto_mo.setText(carSelected.getModelo());
                texto_ve.setText(Integer.toString(carSelected.getVelocidad()));
            }
        }
    }//GEN-LAST:event_combo_1ItemStateChanged

