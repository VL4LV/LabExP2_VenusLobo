    private void boton_crearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_boton_crearMouseClicked
        // TODO add your handling code here:
        String marca = texto_marca.getText();
        String modelo = texto_modelo.getText();
        int velo = (int) velocida.getValue();

        carro nuevoCarro = new carro(marca, modelo, velo);
        carros.add(nuevoCarro);

        texto_marca.setText("");
        texto_modelo.setText("");
        velocida.setValue(1);

        JOptionPane.showMessageDialog(dialogo_crearCarro, "Carro creado exitosamente");

        //guardar el carro en el binario 
        bina.cargarArchivo();
        bina.setCarro(nuevoCarro);
        bina.escribirArchivo();


    }//GEN-LAST:event_boton_crearMouseClicked

